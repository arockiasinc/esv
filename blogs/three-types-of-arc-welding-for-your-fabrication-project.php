                  <div class="bodytext contentText"> <p>Any metal fabrication project will likely need some form of welding to bring the job together.&nbsp;While different in results and applications, all three methods use a metallic wire called an electrode to generate a powerful electric current (“arc”) that melts the bonding material and the base metal (also called parent metal) to create the weld.&nbsp;</p><br><span>1. Stick welding</span><br>Stick welding uses consumable, coated electrodes to create an arc. As the electrode is consumed, the core metal welds the materials together while the electrode flux releases gas to protect the welding area from atmospheric contamination, which could cause oxidation and weaken the bond.<br><br>Stick welding can be used with materials ranging from iron, steel and aluminum to nickel and copper alloys. It’s the best option for outdoor welding since it can be done in any weather and doesn’t require clean working conditions. It’s also often used for DIY projects because the equipment is relatively inexpensive and the technique is easy to learn.<br><br>The downside to stick welding is that it’s messy. It creates substantial debris and leaves molten splatter on the parent metal, requiring a lot of cleaning and sanding.<br><br><span>2. Metal inert gas (MIG) welding</span><br>MIG welding uses a spool gun to feed a consumable wire electrode into the weld area. The gun can also emit a shielding gas (if needed) that protects the weld from contamination. <br><br>MIG welding is fast, efficient, and produces strong, clean welds that require minimum sanding. It can be used with a wide variety of materials, and with different electrode and shielding gas combinations making it a versatile technique. <br><span><br>3. Tungsten inert gas (TIG) welding</span><br>With TIG welding, the welder holds the torch producing the arc in one hand and feeds a consumable metal filler rod into the weld area with the other. A foot pedal is used to control the arc. Like in MIG welding, the torch emits an inert shielding gas, usually argon.<br><br>TIG welding is more difficult to master than the other techniques, but it produces the cleanest, highest quality, and most precise welds. It can be used with a large variety of metals and immaculate working conditions are required.<br><br><span>Metal Fabrication in Winnipeg</span><br>The experienced metal fabrication team at <a href="/" data-track-event="click" data-track-action="internal_link_clicked">East Side Ventilation</a> offers <a href="/residential/furnace-installation-winnipeg" data-track-event="click" data-track-action="internal_link_clicked">stick, MIG and TIG welding</a> in Winnipeg, Brandon, or the surrounding areas. If you require welding services or need to consult an expert for your project, <a href="/winnipeg-hvac-company-contact" data-track-event="click" data-track-action="internal_link_clicked">contact us today</a>. </div>
