function add_lazy_loading_attributes($html)
{
    if (stripos($html, '<img') === false) {
        return $html;
    }

    $segments = preg_split('/(<script\b[^>]*>.*?<\/script>|<style\b[^>]*>.*?<\/style>)/is', $html, -1, PREG_SPLIT_DELIM_CAPTURE);
    if ($segments === false) {
        return $html;
    }

    foreach ($segments as $index => $segment) {
        if ($index % 2 === 1) {
            continue; // Skip script/style blocks
        }

        $segments[$index] = preg_replace_callback(
            '/<img\b(?![^>]*\bloading=)([^>]*?)(\s*\/?)>/i',
            function ($match) {
                $attrs = $match[1];
                if (preg_match('/\bdata-no-lazy\b|\bno-lazy\b|\bskip-lazy\b/i', $attrs)) {
                    return $match[0];
                }
                $closing = $match[2];
                return '<img' . $attrs . ' loading="lazy"' . $closing . '>';
            },
            $segment
        );
    }

    return implode('', $segments);
}

